#Простий калькулятор
first_number = float(input("Введіть перше число:")) #Просимо користувача ввести число1 та переводимо у float
second_number = float(input("Введіть друге число:")) #Просимо користувача ввести число2 та переводимо у float

result = first_number + second_number #Знаходимо суму

print(first_number, "+", second_number, "=", result) #Виводимо результат додавання чисел користувача

