#Конвертер валют

uah = float(input("Введіть суму (грн.): ")) #Просимо користвача ввести суму в гривнях

rate = float(input("Введіть актуальний курс долара: ")) #Просимо користвача ввести суму в гривнях

usd = uah / rate #Ділимо суму на курс
usd = round(usd, 2) #Округлюємо суму

print("Сума в гривнях: ", uah) #Виводимо суму в гривнях
print("Сума в доларах: ", usd) #Виводимо суму в доларах